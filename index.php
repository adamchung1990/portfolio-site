(function($) {
    $(document).ready(function(){
 
        $(window).scroll(function(){
            if ($(this).scrollTop() > 100) {
                $('.rollover').addClass('active');
                $('.desktop-rollover').addClass('active up');
                $('.rollover p').removeClass('hidden');
                $('.logo').fadeIn();

            } else {
            	$('.rollover').removeClass('active');
            	$('.desktop-rollover').removeClass('active up');
            	$('.rollover p').addClass('hidden');
            	$('.logo').fadeOut();
            }
        });


    });
})(jQuery);
 

